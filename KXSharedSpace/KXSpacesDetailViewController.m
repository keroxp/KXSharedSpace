#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 2;
}

- (NSString *)tableView:(UITableView *)tableView titleForHeaderInSection:(NSInteger)section
{
    switch (section) {
        case 0:
            return @"Owners";
            break;
        case 1:
            return @"Values";
        default:
            break;
    }
    return nil;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    switch (section) {
        case 0:
            return [[[[KXSharedSpace sharedSpace] spaceWithName:self.spaceKey] owners] count];
        case 1:
            return [[[[[KXSharedSpace sharedSpace] spaceWithName:self.spaceKey] dictionary] allKeys] count];
        default:
            break;
    }
    return 0;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    
    switch (indexPath.section) {
        case 0: {
            cell.textLabel.text = [[[[[[KXSharedSpace sharedSpace] spaceWithName:self.spaceKey] owners] allObjects] objectAtIndex:indexPath.row] description];
        }
            break;
        case 1: {
            NSString *s = [[[[[KXSharedSpace sharedSpace] spaceWithName:self.spaceKey] dictionary] allKeys] objectAtIndex:indexPath.row];
            cell.textLabel.text = s;
            cell.detailTextLabel.text = [[[[[KXSharedSpace sharedSpace] spaceWithName:self.spaceKey] dictionary] objectForKey:s] description];
        }
            break;
        default:
            break;
    }
    // Configure the cell...
    
    return cell;
}


