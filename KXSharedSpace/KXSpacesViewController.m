#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    return [[[[KXSharedSpace sharedSpace] spaces] allKeys] count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"Cell" forIndexPath:indexPath];
    NSString *key = [[[[KXSharedSpace sharedSpace] spaces] allKeys] objectAtIndex:indexPath.row];
    cell.textLabel.text = key;
    NSUInteger rc = CFGetRetainCount((__bridge CFTypeRef)[[KXSharedSpace sharedSpace] spaceWithName:key]);
    cell.detailTextLabel.text = [NSString stringWithFormat:@"Retain Count : %u",rc];
    // Configure the cell...
    
    return cell;
}


